class Lydi():
    def __init__(self,bezzubost,gitara,gruppa,harizma):
        self.bezzubost=bezzubost
        self.gitara=gitara
        self.gruppa=gruppa
        self.harizma=harizma
        
    def vfvf(self):
        a=self.bezzubost+self.gitara+self.gruppa+self.harizma
        print(a/4)
print("Горшок :")


